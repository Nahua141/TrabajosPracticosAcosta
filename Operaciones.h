
/** \brief Permite el ingreso de los operandos
 *
 * \param float El valor del operando
 * \return float El operando ingresado
 *
 */
float Operando(float);
/** \brief Muestra las operaciones a realizar
 *
 * \param float El valor del primer operando
 * \param float El valor del segndo operando
 * \return void Las operaciones a realizar
 *
 */
void MostrarOperaciones(float, float);
/** \brief Calcula la suma de los operandos
 *
 * \param float El primer operando del cual se hace la suma
 * \param float El segundo operando del cual se hace la suma
 * \return float El resultado de la suma
 *
 */
float Sumar(float, float );
/** \brief Calcula la resta de los operandos
 *
 * \param float El primer operando del cual se hace la resta
 * \param float El segundo operando del cual se hace la resta
 * \return float El resultado de la resta
 *
 */
float Restar(float , float );
/** \brief Calcula la multiplicacion de los operandos
 *
 * \param float El primer operando del cual se hace la multiplicacion
 * \param float El segundo operando del cual se hace la multiplicacion
 * \return float El resultado de la multiplicacion
 *
 */
float Multiplicar(float , float );
/** \brief Calcula la division de los operandos
 *
 * \param float El primer operando del cual se hace la division
 * \param float El segundo operando del cual se hace la division
 * \return float El resultado de la division
 *
 */
float Dividir(float , float );
/** \brief Calcula el factorial de un numero flotante
 *
 * \param float El valor sobre el cual se calcula el factorial
 * \return float El resultado del factorial del numero
 *
 */
float CalcularFactorial(float);
